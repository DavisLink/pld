package com.legvit.pld.stallum.vo;
/**
 * Clase para el almacenamiento de las palabras que contiene un nombre
 * 
 * @author: jmateos
 * @version: 1.0 08/06/2017
 */

public class PalabraNombre {

    private String personaPalabra;
    private int consecutivo;

    public String getPersonaPalabra() {
        return personaPalabra;
    }

    public void setPersonaPalabra(String personaPalabra) {
        this.personaPalabra = personaPalabra;
    }

    public int getConsecutivo() {
        return consecutivo;
    }

    public void setConsecutivo(int consecutivo) {
        this.consecutivo = consecutivo;
    }

  
}
